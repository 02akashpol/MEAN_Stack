
let sumFun = (num1, num2) => {

  return num1 + num2;

};

let diffFun = (num1, num2) => {

  return num1 - num2;

};

let mulFun = (num1, num2) => {

  return num1 * num2;

};

let divFun = (num1, num2) => {

  return num1 / num2;

};

module.exports = {

  sumFun,
  diffFun,
  mulFun,
  divFun
};
